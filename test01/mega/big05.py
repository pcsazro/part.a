# 파이선은 오른쪽부터 읽는다.
# 임의의값(ex.name,age...)들은 ram에 저장된다
name = input("당신의 이름은 :")
age = input("당신의 나이는 :")
gender = input("당신의 성별은 :")
hobby = input("당신의 취미는 :")
life = input("당신의 좌우명은 :")
# 복사 붙여넣기를 이용하면 편하다
print("------------------------------")
print(name, "의", "나이는", age, "세이고", "성별은", gender, "이며")
print("취미는", hobby, "이며", life, "라는 좌우명을 가지고 살아간다")
