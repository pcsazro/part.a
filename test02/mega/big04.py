# 숫자1 : 200
# 숫자2 : 100
# 1)200과 100의 합은 300입니다.
# 2)200과 100의 곱은 20000입니다.

number1 = input("숫자1: ")
number2 = input("숫자2: ")
a = int(number1)
b = int(number2)
print("------------------------------")
print(a,"와",b,"의","합은",a+b,"입니다.")
print(a,"와",b,"의","차는",a-b,"입니다.")
print(a,"와",b,"의","곱은",a*b,"입니다.")
print(a,"와",b,"의","나눗셈은",a/b,"이고","나머지는", a%b,"입니다.")

